Mystr ='My name is Python'
print(Mystr[0:2] )  # 인덱스 0이상, 인덱스 2 미만 슬라이싱  'My' 를 리턴

print(Mystr[3:])  # 인덱스 3 이상부터 끝까지 슬라이싱  'name is Python' 를 리턴

print(Mystr[:7])  # 처음부터 인덱스 7미만까지 슬라이싱 'My name' 를 리턴

print(Mystr[0:-6])  # 처음부터 끝에서 6번째 미만까지 슬라이싱  'My name is '를 리턴

print(Mystr[-6:])  # 끝에서 6번째 부터 , 끝까지 슬라이싱   'Python' 를 리턴

